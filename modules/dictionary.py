    @staticmethod
    def is_valid_noun(word):
        """단어가 유효한 명사인지 검증 (Strict Mode)"""
        if not word:
            return False
            
        # 1. 블랙리스트 (가장 먼저 체크)
        if word in KoreanDictionary.BLACKLIST:
            return False
            
        # 2. 화이트리스트 (여기에 없으면 명사 아님)
        if word in KoreanDictionary.VALID_NOUNS:
            return True
            
        # 3. 영어 대문자 약어는 허용 (예: LLM, AI)
        if re.match(r'^[A-Z]{2,}$', word):
            return True
            
        # 4. 한글 중에서도 '명사형 접미사'가 붙은 경우 일부 허용할 수 있으나
